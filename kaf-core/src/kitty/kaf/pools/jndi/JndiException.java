package kitty.kaf.pools.jndi;

public class JndiException extends Exception {
	private static final long serialVersionUID = 1L;

	public JndiException() {
		super();
	}

	public JndiException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public JndiException(String arg0) {
		super(arg0);
	}

	public JndiException(Throwable arg0) {
		super(arg0);
	}

}
